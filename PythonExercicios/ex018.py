from math import sin, cos, tan, radians
n1 = float(input('Digite um 창ngulo: '))
print('O 창ngulode {} tem o Seno {:.2f}!'.format(n1, sin(radians(n1))))
print('O 창ngulo de {} tem o Cosseno {:.2f}'.format(n1, cos(radians(n1))))
print('O 창ngulo de {} tem o Tangente {:.2f}'.format(n1, tan(radians(n1))))
